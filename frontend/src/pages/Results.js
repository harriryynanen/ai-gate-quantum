
import React, { useContext } from 'react';
import { useNavigate } from 'react-router-dom';
import { SessionContext } from '../context/SessionContext.js';
import { WORKFLOW_STAGES, STAGE_CONFIG } from '../workflow/stages.js';
import WorkflowLayout from '../layouts/WorkflowLayout.js';
import LoadingSpinner from '../components/common/LoadingSpinner.js';
import { LightBulbIcon, DocumentPlusIcon, ArrowPathIcon, CheckCircleIcon, ExclamationTriangleIcon, BeakerIcon, CpuChipIcon } from '@heroicons/react/24/solid';

// Reusable UI Components
const InfoCard = ({ title, children, className }) => (
    <div className={`bg-gray-50 rounded-lg p-4 border border-gray-200 ${className}`}>
        <h3 className="text-md font-semibold text-gray-700 mb-2">{title}</h3>
        <div className="text-sm text-gray-600 space-y-2">{children}</div>
    </div>
);

const Section = ({ title, children }) => (
  <div className="mb-8">
    <h2 className="text-xl font-bold text-gray-800 mb-4 border-b border-gray-200 pb-2">{title}</h2>
    <div className="space-y-4">{children}</div>
  </div>
);

// Main Content Components
const ResultsHeader = ({ result, solver }) => (
    <div className={`text-center p-8 rounded-lg border-2 bg-gray-50 mb-8`}>
        <CheckCircleIcon className={`h-12 w-12 text-green-500 mx-auto mb-4`} />
        <h2 className={`text-3xl font-bold text-gray-900`}>{result.summary || "Results Summary"}</h2>
        <p className={`text-gray-600 mt-2`}>The following results were generated by the <strong>{solver.name}</strong> solver.</p>
    </div>
);

const SummarySection = ({ interpretation, metrics }) => (
    <Section title="Key Findings">
        <InfoCard title="AI Interpretation">
            <p className="text-lg">{interpretation}</p>
        </InfoCard>
        {metrics && 
            <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
                {Object.entries(metrics).map(([key, value]) => (
                    <InfoCard key={key} title={key.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase())}>
                        <p className="text-2xl font-bold text-gray-900">{value}</p>
                    </InfoCard>
                ))}
            </div>
        }
    </Section>
);

const SolverResultBody = ({ result }) => (
    <Section title="Solver Output">
        <div className="bg-gray-800 text-white p-4 rounded-lg text-sm overflow-x-auto">
            <pre>{JSON.stringify(result.data, null, 2)}</pre>
        </div>
    </Section>
);

const TransparencySection = ({ guardrails, narrative }) => (
     <Section title="Transparency & Context">
        <div className="bg-yellow-50 border-l-4 border-yellow-400 p-4">
            <div className="flex">
                <div className="flex-shrink-0">
                    <ExclamationTriangleIcon className="h-5 w-5 text-yellow-500" aria-hidden="true" />
                </div>
                <div className="ml-3">
                    <p className="text-sm font-bold text-yellow-800">Interpretation Guardrails</p>
                    <ul className="list-disc list-inside mt-2 text-sm text-yellow-700">
                        {guardrails.map((note, index) => <li key={index}>{note}</li>)}
                    </ul>
                </div>
            </div>
        </div>
         <InfoCard title="Execution Narrative">
            <p>{narrative}</p>
        </InfoCard>
    </Section>
)

const NextSteps = () => {
    const navigate = useNavigate();
    return (
        <Section title="Next Steps">
            <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <button onClick={() => navigate('/')} className="bg-indigo-600 text-white font-bold py-3 px-4 rounded-lg hover:bg-indigo-700 transition flex items-center justify-center">
                    <DocumentPlusIcon className="h-5 w-5 mr-2" /> Start New Analysis
                </button>
                <button onClick={() => navigate('/')} className="bg-gray-200 text-gray-800 font-bold py-3 px-4 rounded-lg hover:bg-gray-300 transition flex items-center justify-center">
                    <ArrowPathIcon className="h-5 w-5 mr-2"/> Run with Alternative Solver
                </button>
            </div>
        </Section>
    );
}

// Guidance Rail Component
const ResultsGuidance = ({ result }) => {
    return (
        <div>
            <div className="flex items-center text-lg font-bold text-gray-900 mb-4">
                <LightBulbIcon className="h-6 w-6 text-yellow-500 mr-2" />
                <span>Interpreting Your Results</span>
            </div>
            <div className="space-y-4">
                <InfoCard title="What This Result Suggests">
                    <p className="font-semibold">{result.interpretation}</p>
                </InfoCard>
                <InfoCard title="What It Does Not Prove">
                    <p>This result is based on a <strong className="font-semibold">{result.transparencyNotes?.executionNarrative.includes('simulation') ? 'simulated' : 'real'}</strong> backend. While useful for validation, simulated results do not capture the noise and error characteristics of physical quantum hardware.</p>
                </InfoCard>
                 <InfoCard title="Recommended Next Steps">
                    <ul className="list-disc list-inside">
                        <li>Consider if the solution meets your original goal's constraints.</li>
                        <li>Explore running the problem on an alternative solver to compare outcomes.</li>
                        <li>Refine your problem formulation for a more detailed analysis.</li>
                    </ul>
                </InfoCard>
            </div>
        </div>
    );
};


function Results() {
  const {
    session,
    artifacts,
    loading,
    error,
    currentStageConfig,
  } = useContext(SessionContext);

  const stage = currentStageConfig || STAGE_CONFIG[WORKFLOW_STAGES.REVIEW_RESULTS];

  const workflowStages = Object.keys(STAGE_CONFIG).map(key => ({ key, ...STAGE_CONFIG[key] }));

  const renderMainContent = () => {
    if (loading && !artifacts.result) return <LoadingSpinner text="Loading final results..." />;
    if (error) return <p className="text-red-500 text-center p-8">{`Error: ${error.message}`}</p>;
    if (!session || !artifacts.result || !artifacts.recommendation) return <p className="text-gray-500 text-center p-8">Result data is not available for this session.</p>;

    const { result, recommendation } = artifacts;
    const { mappedSolver } = recommendation;

    return (
      <>
        <ResultsHeader result={result} solver={mappedSolver} />
        <SummarySection interpretation={result.interpretation} metrics={result.metrics} />
        <SolverResultBody result={result} />
        <TransparencySection guardrails={result.transparencyNotes.guardrails} narrative={result.transparencyNotes.executionNarrative} />
        <NextSteps />
      </> 
    );
  };
  
  const renderGuidance = () => {
      if (loading || error || !session || !artifacts.result) return null;
      return <ResultsGuidance result={artifacts.result} />;
  }

  return (
    <WorkflowLayout 
      title={stage?.name || 'Loading...'}
      description={`Analysis results for: ${session?.goal || 'your session'}`}
      guidance={renderGuidance()}
      stage={stage}
      session={session}
      workflowStages={workflowStages}
    >
      {renderMainContent()}
    </WorkflowLayout>
  );
}

export default Results;
